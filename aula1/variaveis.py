#VARIAVEIS

#numeros inteiros
numero1 = 1
numero2 = 2
numero3 = 3
numero4 = 4
print(f'numeros inteiros -> {numero1} - {numero2} - {numero3} - {numero4}')

#numeros decimais
float1 = 1.1
float2 = 2.2
float3 = 3.3
float4 = 4.4
print(f'numeros decimais -> {float1} - {float2} - {float3} - {float4}')

#textos
string1 = 'um'
string2 = 'dois'
string3 = 'tres'
string4 = 'quatro'
print(f'tipos de textos -> {string1} - {string2} - {string3} - {string4}')

#booleanos
verdadeiro = True
falso = False
print(f'tipos booleanos -> {verdadeiro} - {falso}')

#TIPOS 
print(type(numero1))
print(type(float1))
print(type(string1))
print(type(verdadeiro))
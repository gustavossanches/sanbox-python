# CONDICIONAIS
'''
!=   | diferente de
==   | igual a
>    | maior que
>=   | maior ou igual que
<    | menor que
<=   | menor ou igual que

'''

#inicia a variavel ' x ' com valor 5
x = 5

if(x != 4):
    #se atender ao requisito entra aqui
    print('x é diferente de 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é diferente de 4')
################################################################
if(x == 4):
    #se atender ao requisito entra aqui
    print('x é igual a 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é igual a 4')
################################################################
if(x > 4):
    #se atender ao requisito entra aqui
    print('x é maior que 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é maior que 4')
################################################################
if(x >= 4):
    #se atender ao requisito entra aqui
    print('x é maior ou igual a 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é maior ou igual a 4')
################################################################
if(x < 4):
    #se atender ao requisito entra aqui
    print('x é menor que 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é menor que')
################################################################
if(x <= 4):
    #se atender ao requisito entra aqui
    print('x é menor ou igual a 4')
else:
    #se não atender ao requisito, entra aqui
    print('x não é menor ou igual a 4')
################################################################
if(x < 4):
    #se atender ao requisito entra aqui
    print('x é menor ou igual a 4')
elif(x < 2):
    #verifica uma segunda alternativa
    print('x é maior ou igual que 2')
else:
    #se não atender ao requisito, entra aqui
    print('x é menor que 2')
    
    
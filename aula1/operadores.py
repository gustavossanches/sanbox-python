#OPERADORES

'''

+    | soma
-    | subtação
*    | multiplicação
**   | exponenciação
/    | divisão
%    | modulo

'''

num1 = 5
num2 = 7

soma = num1 + num2
multiplicaçao = num1 + num2
subtracao = num1 - num2
modulo = num1 / num2
exp = num1 ** num2
modulo = num1 % num2

print(f'soma          -> {soma}')
print(f'subtracão     -> {subtracao}')
print(f'multiplicação -> {multiplicaçao}')
print(f'exp           -> {exp}')
print(f'modulo        -> {modulo}')